a = int(input('Digite um número: '))
b = int(input('Digite outro número: '))
soma = a + b
subtr = a - b
multi = a * b
divisao = a / b
resto = a % b
print('Soma:{}'
      '\nSubtração: {}'
      '\nMultiplaicação: {}'
      '\nDivisão: {}'
      '\nResto: {}'.format(soma, subtr, multi, int(divisao), resto))
